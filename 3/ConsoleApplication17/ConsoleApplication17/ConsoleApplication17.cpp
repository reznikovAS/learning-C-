#include <iostream>

// Функция для нахождения наибольшего общего делителя (НОД)
int gcd(int a, int b) {
    while (b != 0) {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

int main() {
    setlocale(LC_ALL, "RUS");

    // Задаем максимальное значение для знаменателя
    const int maxDenominator = 7;

    // Перебор всех возможных пар числителя и знаменателя
    for (int denominator = 2; denominator <= maxDenominator; ++denominator) {
        for (int numerator = 1; numerator < denominator; ++numerator) {
            // Проверка на простоту и несократимость
            if (gcd(numerator, denominator) == 1) {
                std::cout << numerator << "/" << denominator << " ";
            }
        }
    }

    std::cout << std::endl;

    return 0;
}


