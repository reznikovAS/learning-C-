//
#include <iostream>
#include <cmath>

int main() {
    setlocale(LC_ALL, "Russian");

    int n;

    // Ввод значения n
    std::cout << "Введите значение n: ";
    std::cin >> n;

    long double result = 0.0;

    // Вычисление суммы
    for (int k = 1; k <= n; ++k) {
        int sign = pow(-1, k);
        long double term = 1.0;

        // Последовательность чисел (2k^2 + 1)
        for (int i = 1; i <= 2 * k * k + 1; ++i) {
            term *= i;
        }

        result += sign * term;
    }

    // Вывод результата
    std::cout << "Результат суммы для n = " << n << ": " << result << std::endl;

    return 0;
}




