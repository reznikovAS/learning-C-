// ConsoleApplication11.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
#include <cstdlib>  // Для использования функции rand
#include <algorithm>  // Для использования функции std::sort
#include <ctime>    // Для использования функции time

int main() {
    setlocale(LC_ALL, "RUS");

    // Ввод размера массива
    int n;
    std::cout << "Введите размер массива: ";
    std::cin >> n;

    if (n <= 0) {
        std::cout << "Размер массива должен быть положительным числом. Программа завершена." << std::endl;
        return 1;
    }

    // Создание массива и выбор способа заполнения
    int* array = new int[n];

    std::cout << "Выберите способ заполнения массива:" << std::endl;
    std::cout << "1. Ввести элементы самостоятельно" << std::endl;
    std::cout << "2. Сгенерировать случайные числа" << std::endl;

    int choice;
    std::cin >> choice;

    if (choice == 1) {
        // Ввод элементов самостоятельно
        std::cout << "Введите элементы массива:" << std::endl;
        for (int i = 0; i < n; ++i) {
            std::cin >> array[i];
        }
    }
    else if (choice == 2) {
        // Генерация случайных чисел от -10 до 10
        std::cout << "Исходный массив: ";
        for (int i = 0; i < n; ++i) {
            array[i] = rand() % 21 - 10;  // Генерация случайных чисел от -10 до 10
            std::cout << array[i] << " ";
        }
        std::cout << std::endl;
    }
    else {
        // Некорректный выбор, завершаем программу
        std::cout << "Некорректный выбор. Программа завершена." << std::endl;
        delete[] array;
        return 1;
    }

    // Сортировка массива по убыванию
    std::sort(array, array + n, std::greater<int>());

    // Вывод отсортированного массива
    std::cout << "Отсортированный массив по убыванию:" << std::endl;
    for (int i = 0; i < n; ++i) {
        std::cout << array[i] << " ";
    }
    std::cout << std::endl;

    // Просьба ввести число N
    int N;
    std::cout << "Введите число N: ";
    std::cin >> N;

    // Вывод массива с добавлением числа N и упорядоченным по убыванию
    std::cout << "Массив с добавлением числа N и упорядоченный по убыванию:" << std::endl;
    array[n] = N;
    std::sort(array, array + n + 1, std::greater<int>());
    for (int i = 0; i < n + 1; ++i) {
        std::cout << array[i] << " ";
    }
    std::cout << std::endl;

  

    return 0;
}



