// ConsoleApplication12.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
#include <cstdlib>  // Для использования функции rand
#include <ctime>    // Для использования функции time

int main() {
    setlocale(LC_ALL, "RUS");

    // Ввод размера массива
    int n;
    std::cout << "Введите размер массива: ";
    std::cin >> n;

    if (n <= 0) {
        std::cout << "Размер массива должен быть положительным числом. Программа завершена." << std::endl;
        return 1;
    }

    // Создание массива и генерация элементов
    int* array = new int[n + 1];  // Увеличиваем размер массива на 1 для учета сдвига
    std::cout << "Выберите способ заполнения массива:" << std::endl;
    std::cout << "1. Ввести элементы самостоятельно" << std::endl;
    std::cout << "2. Сгенерировать случайные числа" << std::endl;

    int choice;
    std::cin >> choice;

    if (choice == 1) {
        // Ввод элементов самостоятельно
        std::cout << "Введите элементы массива:" << std::endl;
        for (int i = 1; i <= n; ++i) {
            std::cin >> array[i];
        }
    }
    else if (choice == 2) {
        // Генерация случайных чисел от -10 до 10
        std::cout << "Исходный массив: ";
        for (int i = 1; i <= n; ++i) {
            array[i] = rand() % 21 - 10;  // Генерация случайных чисел от -10 до 10
            std::cout << array[i] << " ";
        }
        std::cout << std::endl;
    }
    else {
        // Некорректный выбор, завершаем программу
        std::cout << "Некорректный выбор. Программа завершена." << std::endl;
        delete[] array;
        return 1;
    }

    // Преобразование массива
    int* resultArray = new int[n + 1];  // Также увеличиваем размер для учета сдвига
    int index = 1;  // Начинаем с 1

    // Перебор элементов в четных позициях (первая половина массива)
    for (int i = 2; i <= n; i += 2) {
        resultArray[index] = array[i];
        ++index;
    }

    // Перебор элементов в нечетных позициях (вторая половина массива)
    for (int i = 1; i <= n; i += 2) {
        resultArray[index] = array[i];
        ++index;
    }

    // Вывод результата
    std::cout << "Преобразованный массив:" << std::endl;
    for (int i = 1; i <= n; ++i) {
        std::cout << resultArray[i] << " ";
    }
    std::cout << std::endl;

    // Освобождение памяти
    delete[] array;
    delete[] resultArray;

    return 0;
}



