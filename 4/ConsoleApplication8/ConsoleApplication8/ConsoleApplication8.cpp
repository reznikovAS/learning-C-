// ConsoleApplication8.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
#include <ctime>
#include <cstdlib>

int main() {
    setlocale(LC_ALL, "RUS");

    int size;

    // Ввод размера массива
    std::cout << "Введите размер массива: ";
    std::cin >> size;

    if (size <= 0) {
        std::cout << "Размер массива должен быть положительным числом. Программа завершена." << std::endl;
        return 1;
    }

    // Динамическое выделение памяти под массив
    int* array = new int[size];

    // Установка начального значения для генератора случайных чисел
    std::srand(std::time(0));

    // Вопрос пользователю
    std::cout << "Выберите способ заполнения массива:" << std::endl;
    std::cout << "1. Ввести числа самостоятельно" << std::endl;
    std::cout << "2. Сгенерировать случайные числа" << std::endl;

    int choice;
    std::cin >> choice;

    if (choice == 1) {
        // Ввод чисел самостоятельно
        std::cout << "Введите " << size << " чисел:" << std::endl;
        for (int i = 0; i < size; ++i) {
            std::cin >> array[i];
        }
    }
    else if (choice == 2) {
        // Генерация случайных чисел от -10 до 10
        std::cout << "Исходный массив: ";
        for (int i = 0; i < size; ++i) {
            array[i] = std::rand() % 21 - 10;
            std::cout << array[i] << " ";
        }
        std::cout << std::endl;
    }
    else {
        // Некорректный выбор, завершаем программу
        std::cout << "Некорректный выбор. Программа завершена." << std::endl;

        // Освобождение выделенной памяти
        delete[] array;

        return 1;
    }

    // Замена чисел, кратных 3, их кубами
    for (int i = 0; i < size; ++i) {
        if (array[i] % 3 == 0) {
            array[i] = array[i] * array[i] * array[i];
        }
    }

    // Вывод измененного массива
    std::cout << "Измененный массив: ";
    for (int i = 0; i < size; ++i) {
        std::cout << array[i] << " ";
    }
    std::cout << std::endl;

    // Освобождение выделенной памяти
    delete[] array;

    return 0;
}



