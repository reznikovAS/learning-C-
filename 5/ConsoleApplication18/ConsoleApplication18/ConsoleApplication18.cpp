// ConsoleApplication18.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
#include <vector>
#include <cstdlib>  // Для использования функции rand
#include <ctime>    // Для установки начального значения генератора случайных чисел

int main() {
    setlocale(LC_ALL, "RUS");

    // Ввод размеров матрицы N и M
    int N, M;

    std::cout << "Введите количество строк N: ";
    std::cin >> N;

    std::cout << "Введите количество столбцов M: ";
    std::cin >> M;

    // Вопрос пользователю
    std::cout << "Выберите способ заполнения матрицы:" << std::endl;
    std::cout << "1. Ввести элементы вручную" << std::endl;
    std::cout << "2. Сгенерировать случайные числа" << std::endl;

    int choice;
    std::cin >> choice;

    // Создание матрицы
    std::vector<std::vector<int>> matrix(N, std::vector<int>(M));

    if (choice == 1) {
        // Ручной ввод
        std::cout << "Введите элементы матрицы:" << std::endl;
        for (int i = 0; i < N; ++i) {
            for (int j = 0; j < M; ++j) {
                std::cin >> matrix[i][j];
            }
        }
    }
    else if (choice == 2) {
        // Генерация случайных чисел в интервале от -10 до 10
        std::srand(std::time(0)); // Установка начального значения для генератора случайных чисел

        for (int i = 0; i < N; ++i) {
            for (int j = 0; j < M; ++j) {
                matrix[i][j] = std::rand() % 21 - 10;  // Генерация случайных чисел от -10 до 10
            }
        }
    }
    else {
        // Некорректный выбор, завершаем программу
        std::cout << "Некорректный выбор. Программа завершена." << std::endl;
        return 1;
    }

    // Вывод матрицы
    std::cout << "Матрица:" << std::endl;
    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < M; ++j) {
            std::cout << matrix[i][j] << " ";
        }
        std::cout << std::endl;
    }

    // Поиск среднего арифметического для каждой строки и подсчет элементов, меньших среднего
    for (int i = 0; i < N; ++i) {
        int sum = 0;
        for (int j = 0; j < M; ++j) {
            sum += matrix[i][j];
        }

        double average = static_cast<double>(sum) / M;

        int count = 0;
        for (int j = 0; j < M; ++j) {
            if (matrix[i][j] < average) {
                count++;
            }
        }

        std::cout << "В строке " << i + 1 << " количество элементов меньших среднего: " << count << std::endl;
    }

    return 0;
}

