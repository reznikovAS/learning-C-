// ConsoleApplication19.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
#include <vector>
#include <cstdlib>  // Для использования функции rand
#include <ctime>    // Для установки начального значения генератора случайных чисел

int main() {
    setlocale(LC_ALL, "RUS");

    // Ввод порядка матрицы M
    int M;

    std::cout << "Введите порядок матрицы M: ";
    std::cin >> M;

    // Вопрос пользователю
    std::cout << "Выберите способ заполнения матрицы:" << std::endl;
    std::cout << "1. Ввести элементы вручную" << std::endl;
    std::cout << "2. Сгенерировать случайные числа" << std::endl;

    int choice;
    std::cin >> choice;

    // Создание квадратной матрицы
    std::vector<std::vector<int>> matrix(M, std::vector<int>(M));

    if (choice == 1) {
        // Ручной ввод
        std::cout << "Введите элементы матрицы:" << std::endl;
        for (int i = 0; i < M; ++i) {
            for (int j = 0; j < M; ++j) {
                std::cin >> matrix[i][j];
            }
        }
    }
    else if (choice == 2) {
        // Генерация случайных чисел в интервале от -10 до 10
        std::srand(std::time(0)); // Установка начального значения для генератора случайных чисел

        for (int i = 0; i < M; ++i) {
            for (int j = 0; j < M; ++j) {
                matrix[i][j] = rand() % 21 - 10;  // Генерация случайных чисел от -10 до 10
            }
        }
    }
    else {
        // Некорректный выбор, завершаем программу
        std::cout << "Некорректный выбор. Программа завершена." << std::endl;
        return 1;
    }

    // Замена нулями элементов, лежащих ниже побочной диагонали
    for (int i = 0; i < M; ++i) {
        for (int j = 0; j < M; ++j) {
            if (i + j > M - 1) {
                matrix[i][j] = 0;
            }
        }
    }

    // Вывод обновленной матрицы
    std::cout << "Обновленная матрица:" << std::endl;
    for (int i = 0; i < M; ++i) {
        for (int j = 0; j < M; ++j) {
            std::cout << matrix[i][j] << " ";
        }
        std::cout << std::endl;
    }

    return 0;
}


