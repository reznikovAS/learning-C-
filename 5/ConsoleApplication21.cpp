// ConsoleApplication21.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//

#include <iostream>
#include <vector>
#include <cstdlib>  // Для использования функции rand
#include <ctime>    // Для установки начального значения генератора случайных чисел

void printMatrix(const std::vector<std::vector<int>>& matrix) {
    for (const auto& row : matrix) {
        for (int value : row) {
            std::cout << value << " ";
        }
        std::cout << std::endl;
    }
}

int main() {
    setlocale(LC_ALL, "RUS");

    // Ввод размеров матрицы M и N
    int M, N;

    std::cout << "Введите количество строк M: ";
    std::cin >> M;

    std::cout << "Введите количество столбцов N: ";
    std::cin >> N;

    // Создание матрицы и матрицы-копии
    std::vector<std::vector<int>> matrix(M, std::vector<int>(N));
    std::vector<std::vector<int>> matrixCopy(M, std::vector<int>(N));

    // Вопрос пользователю
    std::cout << "Выберите способ заполнения матрицы:" << std::endl;
    std::cout << "1. Ввести элементы вручную" << std::endl;
    std::cout << "2. Сгенерировать случайные числа" << std::endl;

    int choice;
    std::cin >> choice;

    if (choice == 1) {
        // Ручной ввод элементов матрицы
        std::cout << "Введите элементы матрицы:" << std::endl;
        for (int i = 0; i < M; ++i) {
            for (int j = 0; j < N; ++j) {
                std::cin >> matrix[i][j];
                matrixCopy[i][j] = matrix[i][j];  // Копируем матрицу для последующего вывода
            }
        }
    }
    else if (choice == 2) {
        // Генерация случайных чисел в интервале от -10 до 10
        std::srand(std::time(0)); // Установка начального значения для генератора случайных чисел

        for (int i = 0; i < M; ++i) {
            for (int j = 0; j < N; ++j) {
                matrix[i][j] = rand() % 21 - 10;  // Генерация случайных чисел от -10 до 10
                matrixCopy[i][j] = matrix[i][j];  // Копируем матрицу для последующего вывода
            }
        }
    }
    else {
        // Некорректный выбор, завершаем программу
        std::cout << "Некорректный выбор. Программа завершена." << std::endl;
        return 1;
    }

    // Вывод изначальной матрицы
    std::cout << "Изначальная матрица:" << std::endl;
    printMatrix(matrix);

    // Находим самые нижние максимальные элементы в столбцах
    std::vector<int> lowestMaxInColumn(N, INT_MIN); // Инициализация массива минимальными значениями

    for (int j = 0; j < N; ++j) {
        for (int i = M - 1; i >= 0; --i) {
            if (matrix[i][j] > lowestMaxInColumn[j]) {
                lowestMaxInColumn[j] = matrix[i][j];
            }
        }
    }

    // Переставляем столбцы так, чтобы максимальные элементы расположились на главной диагонали
    for (int j = 0; j < N; ++j) {
        for (int i = M - 1; i >= 0; --i) {
            if (matrix[i][j] == lowestMaxInColumn[j]) {
                // Меняем столбцы местами
                for (int k = 0; k < M; ++k) {
                    int temp = matrix[k][j];
                    matrix[k][j] = matrix[k][N - 1];
                    matrix[k][N - 1] = temp;
                }
                break; // Переходим к следующему столбцу
            }
        }
    }

    // Вывод обновленной матрицы
    std::cout << "Обновленная матрица:" << std::endl;
    printMatrix(matrix);

    return 0;
}


